// Setting CONTROL layer RGB back to default
void update_tri_layer_RGB(uint8_t layer1, uint8_t layer2, uint8_t layer3) {
  if (IS_LAYER_ON(layer1) && IS_LAYER_ON(layer2)) {
    layer_on(layer3);
  } else {
    layer_off(layer3);
  }
}



 bool encoder_update_user(uint8_t index, bool clockwise) {
     if (index == 0) {
         // Base layer (you can also use the enums in `enum layer_names` instead of 0, 1, 2, etc.)
         if (layer_state_is(_BASE)) {
            if (clockwise) {
                 tap_code(KC_VOLU);
             } else {
                 tap_code(KC_VOLD);
             }
         } else if (layer_state_is(_NAVIGATE) || layer_state_is(_SYMBOLS)) {
             if (clockwise) {
                 tap_code16(LCTL(KC_PPLS));
             } else {
                 tap_code16(LCTL(KC_PMNS));
             }
         }
     }
     else if (index == 1) {
         if (layer_state_is(_BASE)) {
             if (clockwise) {
                 tap_code16(LCTL(KC_PPLS));
             } else {
                 tap_code16(LCTL(KC_PMNS));
             }

         } else if (layer_state_is(_NAVIGATE) || layer_state_is(_SYMBOLS)) {
             if (clockwise) {
                 tap_code(KC_PGDN);
             } else {
                 tap_code(KC_PGUP);
             }
        }
     }
     return false;
}

uint8_t active_layer(void) {
    if(layer_state_is(_CONTROL)) {
            return _CONTROL;
    }
#if MOUSEKEY_ENABLE
    if(layer_state_is(_MOUSE)) {
        return _MOUSE;
    }
#endif
    if(layer_state_is(_NAVIGATE)) {
        return _NAVIGATE;
    }
    if(layer_state_is(_SYMBOLS)) {
        return _SYMBOLS;
    }
    if(layer_state_is(_NUM)) {
        return _NUM;
    }
    if(layer_state_is(_FUN)) {
        return _FUN;
    }
    if(layer_state_is(_GAME)) {
        return _GAME;
    }
    return _BASE;

}
