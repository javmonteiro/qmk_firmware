void rgb_mute_section(uint8_t min, uint8_t max) {
  for (uint8_t i = min; i <= max; i++) {
        rgb_matrix_set_color(i, 0,0,0);
  }
}



void rgb_section(uint8_t min, uint8_t max, uint8_t h, uint8_t s) {
  uint8_t v=rgb_matrix_get_hsv().v;
  RGB rgb = hsv_to_rgb((HSV) { h,s,v });
  for (uint8_t i = min; i <= max; i++) {
        rgb_matrix_set_color(i, rgb.r,rgb.g,rgb.b);
  }
}


bool  rgb_matrix_indicators_advanced_user(uint8_t led_min, uint8_t led_max) {
    uint8_t layer=active_layer();
    RGB rgb = hsv_to_rgb((HSV) { 255,128,128});

    uint8_t v=rgb_matrix_get_hsv().v;
    if (layer==_BASE) {
        return true;
    }
    switch(layer) {
        case _CONTROL:
            rgb = hsv_to_rgb((HSV) { COLOR_CONTROL,v});
            break;
        case _SYMBOLS:
            rgb_mute_section(led_min,14);
            rgb_section(13,25,COLOR_CYAN);
            rgb_section(25,30,COLOR_PURPLE);
            rgb_mute_section(30,44);
            rgb_section(44,54,COLOR_CYAN);
            rgb_section(54,led_max,COLOR_PURPLE);
            return true;
        case _NAVIGATE:
            rgb_mute_section(led_min,14);
            rgb_section(14,17,COLOR_YELLOW);
            rgb_mute_section(18,44);
            rgb_section(44,48,COLOR_YELLOW);
            rgb_mute_section(48,50);
            rgb_section(50,54,COLOR_PURPLE);
            rgb_mute_section(54,56);
            rgb_section(56,58,COLOR_CYAN);
            rgb_mute_section(58,led_max);
            return true;
//        case _NUM:
//            rgb = hsv_to_rgb((HSV) { COLOR_NUM,v});
//            break;
#ifdef MOUSEKEY_ENABLE
        case _MOUSE:
             rgb = hsv_to_rgb((HSV) {  COLOR_MOUSE,v  });
             break;
 #endif
        case _FUN:
             rgb = hsv_to_rgb((HSV) {  COLOR_FUNCTION,v  });
            break;
        case _GAME:
            rgb = hsv_to_rgb((HSV) { COLOR_YELLOW ,v});
            break;
        default:
            rgb_mute_section(30,44);
            rgb_section(44,led_max,COLOR_PURPLE);
            return true;
    }
    for (uint8_t i = led_min; i < led_max; i++) {
        if (disable_underglow) {
            if (HAS_FLAGS(g_led_config.flags[i], LED_FLAG_UNDERGLOW)) {
                continue;
            }
        }
        if (HAS_FLAGS(g_led_config.flags[i], LED_FLAG_MODIFIER)) {
            rgb_matrix_set_color(i, RGB_BLACK);
        } else {
            rgb_matrix_set_color(i, rgb.r, rgb.g, rgb.b);
        }
    }
   return true;
}
