uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case HOME_S:
        case HOME_D:
        case HOME_F:
        case HOME_J:
        case HOME_K:
        case HOME_L:
            return TAPPING_TERM ;
        default:
            return TAPPING_TERM * 2;
    }
}


void caps_word_set_user(bool active) {
    setPinOutput(24);
    // Turn the LED off
    // (Due to technical reasons, high is off and low is on)
    if (active) {
        writePinLow(24);
    } else {
        writePinHigh(24);
    }
}


